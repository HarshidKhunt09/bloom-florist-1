/**
 * Generates out/_redirects after Next.js static export.
 *
 * Strategy:
 *  - All known page + blog routes are listed as 200 rewrites pointing to their own path.
 *    This stops Cloudflare from falling through to the catch-all for existing pages.
 *  - /index.html is also explicitly listed to prevent the internal rewrite loop:
 *    (/ → /index.html 200 → CF re-checks /index.html → /* / 301 → loop).
 *  - Catch-all /* → / 301 at the end handles truly unknown URLs.
 */

const https = require('https');
const http  = require('http');
const fs    = require('fs');
const path  = require('path');

const STRAPI_URL   = process.env.NEXT_PUBLIC_STRAPI_API_URL   || '';
const STRAPI_TOKEN = process.env.NEXT_PUBLIC_STRAPI_API_TOKEN || '';
const DOMAIN       = process.env.NEXT_PUBLIC_HARDCODED_DOMAIN || '';
const OUT_DIR      = path.join(__dirname, '..', 'out');

function fetchJson(url, token) {
  return new Promise((resolve, reject) => {
    const mod = url.startsWith('https') ? https : http;
    mod.get(url, { headers: { Authorization: `Bearer ${token}` } }, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        try { resolve(JSON.parse(data)); }
        catch (e) { reject(e); }
      });
    }).on('error', reject);
  });
}

async function main() {
  if (!STRAPI_URL || !DOMAIN) {
    console.log('[redirects] Env vars missing — writing minimal _redirects.');
    writeFallback();
    return;
  }

  let articles = [];
  try {
    const result = await fetchJson(STRAPI_URL, STRAPI_TOKEN);
    articles = result.data || [];
  } catch (e) {
    console.warn('[redirects] Could not fetch Strapi articles:', e.message);
  }

  // Collect all known slugs for this domain
  const knownSlugs = new Set(['/']);
  for (const article of articles) {
    const slug = article?.attributes?.urlSlug;
    if (slug && article?.attributes?.Domain === DOMAIN) {
      const clean = '/' + slug.replace(/^\/+/, '');
      knownSlugs.add(clean);
    }
  }

  const lines = [
    '# Auto-generated by scripts/generate-redirects.js',
    '',
    '# Prevent internal rewrite loop: /index.html must not hit the catch-all',
    '/index.html /index.html 200',
    '',
    '# Known routes — 200 rewrite keeps URL unchanged, serves static file',
  ];

  for (const slug of [...knownSlugs].sort()) {
    if (slug === '/') {
      lines.push('/  /index.html  200');
    } else {
      const htmlPath = slug.replace(/\/$/, '') + '/index.html';
      lines.push(`${slug}  ${htmlPath}  200`);
    }
  }

  lines.push('');
  lines.push('# Catch-all: unknown URLs → home with 301');
  lines.push('/*  /  301');
  lines.push('');

  writeFile(lines.join('\n'));
  console.log(`[redirects] Written _redirects: ${knownSlugs.size} routes + /index.html guard + catch-all 301`);
}

function writeFallback() {
  writeFile([
    '/index.html /index.html 200',
    '/* /index.html 200',
    '',
  ].join('\n'));
}

function writeFile(content) {
  if (!fs.existsSync(OUT_DIR)) {
    console.warn('[redirects] out/ dir not found — skipping.');
    return;
  }
  fs.writeFileSync(path.join(OUT_DIR, '_redirects'), content, 'utf8');
  console.log(content);
}

main().catch(e => {
  console.error('[redirects] Error:', e.message);
  writeFallback();
});
