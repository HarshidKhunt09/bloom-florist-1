/**
 * Generates out/_redirects after Next.js static export.
 *
 * Strategy:
 *  - Scans the actual out/ directory for built index.html files — these are
 *    ground truth for what pages exist. Strapi slugs supplement this list but
 *    the filesystem is authoritative.
 *  - Known routes are listed first as 200 rewrites (first-match wins in CF Pages).
 *  - Unknown paths are caught by /:splat and /:splat/* 301 rules.
 *    Using named params instead of /* avoids matching root / (which would
 *    cause an infinite 301 self-redirect loop).
 *  - / is never matched by the catch-alls; CF Pages serves index.html naturally.
 */

const https = require('https');
const http  = require('http');
const fs    = require('fs');
const path  = require('path');

const STRAPI_URL   = process.env.NEXT_PUBLIC_STRAPI_API_URL   || '';
const STRAPI_TOKEN = process.env.NEXT_PUBLIC_STRAPI_API_TOKEN || '';
const DOMAIN       = process.env.NEXT_PUBLIC_HARDCODED_DOMAIN || '';
const OUT_DIR      = path.join(__dirname, '..', 'out');

function fetchJson(url, token) {
  return new Promise((resolve, reject) => {
    const mod = url.startsWith('https') ? https : http;
    mod.get(url, { headers: { Authorization: `Bearer ${token}` } }, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        try { resolve(JSON.parse(data)); }
        catch (e) { reject(e); }
      });
    }).on('error', reject);
  });
}

/** Scan out/ and return every top-level slug that has an index.html */
function scanBuiltPages() {
  const slugs = new Set();
  if (!fs.existsSync(OUT_DIR)) return slugs;
  for (const entry of fs.readdirSync(OUT_DIR, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    if (entry.name.startsWith('_') || entry.name.startsWith('.')) continue;
    const indexFile = path.join(OUT_DIR, entry.name, 'index.html');
    if (fs.existsSync(indexFile)) {
      slugs.add('/' + entry.name);
    }
  }
  return slugs;
}

async function main() {
  // Always scan built output — this is ground truth regardless of Strapi
  const knownSlugs = scanBuiltPages();
  console.log(`[redirects] Found ${knownSlugs.size} built pages in out/`);

  // Supplement with Strapi slugs (catches any dynamic routes that might be
  // at nested paths not visible at the top level of out/)
  if (STRAPI_URL && DOMAIN) {
    try {
      const result = await fetchJson(STRAPI_URL, STRAPI_TOKEN);
      const articles = result.data || [];
      for (const article of articles) {
        const slug = article?.attributes?.urlSlug;
        if (slug && article?.attributes?.Domain === DOMAIN) {
          const clean = '/' + slug.replace(/^\/+/, '');
          if (clean !== '/') knownSlugs.add(clean);
        }
      }
    } catch (e) {
      console.warn('[redirects] Could not fetch Strapi articles:', e.message);
    }
  } else {
    console.log('[redirects] STRAPI_URL/DOMAIN not set — using filesystem scan only.');
  }

  const lines = [
    '# Auto-generated by scripts/generate-redirects.js',
    '',
    '# Known routes — listed before the catch-all so they are never redirected.',
    '# Source: out/ directory scan + Strapi articles.',
  ];

  for (const slug of [...knownSlugs].sort()) {
    const htmlPath = slug.replace(/\/$/, '') + '/index.html';
    lines.push(`${slug}  ${htmlPath}  200`);
  }

  lines.push('');
  lines.push('# Catch-all: truly unknown paths → home with 301.');
  lines.push('# /:splat (named param) never matches bare / so the root never loops.');
  lines.push('/:splat  /  301');
  lines.push('/:splat/*  /  301');
  lines.push('');

  writeFile(lines.join('\n'));
  console.log(`[redirects] Written _redirects: ${knownSlugs.size} known routes + 2-rule catch-all`);
}

function writeFallback() {
  // Minimal fallback when out/ doesn't exist yet
  writeFile([
    '# Catch-all: unknown paths → home 301 (/:splat avoids matching root /)',
    '/:splat  /  301',
    '/:splat/*  /  301',
    '',
  ].join('\n'));
}

function writeFile(content) {
  if (!fs.existsSync(OUT_DIR)) {
    console.warn('[redirects] out/ dir not found — skipping.');
    return;
  }
  fs.writeFileSync(path.join(OUT_DIR, '_redirects'), content, 'utf8');
  console.log(content);
}

main().catch(e => {
  console.error('[redirects] Error:', e.message);
  writeFallback();
});
