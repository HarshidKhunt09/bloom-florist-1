/**
 * Generates out/_redirects after Next.js static export.
 *
 * Strategy:
 *  - Scans the actual out/ directory for built files — ground truth for what pages exist.
 *    Handles both trailingSlash formats:
 *      trailingSlash: true  → out/about/index.html  → rule: /about  /about/index.html  200
 *      trailingSlash: false → out/about.html         → rule: /about  /about.html         200
 *  - Strapi slugs supplement (catches any routes the FS scan misses).
 *  - Known routes listed first as 200 rewrites (first-match wins in CF Pages).
 *  - Unknown paths caught by /:splat and /:splat/* 301 rules.
 *    Named params never match bare / so root never self-redirects.
 */

const https = require('https');
const http  = require('http');
const fs    = require('fs');
const path  = require('path');

const STRAPI_URL   = process.env.NEXT_PUBLIC_STRAPI_API_URL   || '';
const STRAPI_TOKEN = process.env.NEXT_PUBLIC_STRAPI_API_TOKEN || '';
const DOMAIN       = process.env.NEXT_PUBLIC_HARDCODED_DOMAIN || '';
const OUT_DIR      = path.join(__dirname, '..', 'out');

function fetchJson(url, token) {
  return new Promise((resolve, reject) => {
    const mod = url.startsWith('https') ? https : http;
    mod.get(url, { headers: { Authorization: `Bearer ${token}` } }, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        try { resolve(JSON.parse(data)); }
        catch (e) { reject(e); }
      });
    }).on('error', reject);
  });
}

/**
 * Scan out/ and return a Map of { slug → target file path }.
 * Handles both trailingSlash:true (dir/index.html) and false (page.html).
 */
function scanBuiltPages() {
  const routes = new Map(); // slug → CF target path
  if (!fs.existsSync(OUT_DIR)) return routes;

  const SKIP = new Set(['index.html', '404.html', '404', '_next', '_redirects', 'sitemap.xml', 'sitemap', 'robots.txt']);

  for (const entry of fs.readdirSync(OUT_DIR, { withFileTypes: true })) {
    const name = entry.name;
    if (name.startsWith('_') || name.startsWith('.') || SKIP.has(name)) continue;

    if (entry.isDirectory()) {
      // trailingSlash: true format → out/about/index.html
      const indexFile = path.join(OUT_DIR, name, 'index.html');
      if (fs.existsSync(indexFile)) {
        routes.set('/' + name, '/' + name + '/index.html');
      }
    } else if (name.endsWith('.html')) {
      // trailingSlash: false format → out/about.html
      const slug = '/' + name.replace(/\.html$/, '');
      routes.set(slug, '/' + name);
    }
  }
  return routes;
}

async function main() {
  const routes = scanBuiltPages(); // Map<slug, targetFile>
  console.log(`[redirects] Found ${routes.size} built pages in out/: ${[...routes.keys()].join(', ') || 'none'}`);

  // Supplement with Strapi slugs pointing to their likely target
  if (STRAPI_URL && DOMAIN) {
    try {
      const result = await fetchJson(STRAPI_URL, STRAPI_TOKEN);
      const articles = result.data || [];
      let added = 0;
      for (const article of articles) {
        const slug = article?.attributes?.urlSlug;
        if (!slug || article?.attributes?.Domain !== DOMAIN) continue;
        const clean = '/' + slug.replace(/^\/+/, '');
        if (clean === '/' || routes.has(clean)) continue;
        // Determine target — prefer .html (default Next.js export)
        const htmlFile = path.join(OUT_DIR, slug.replace(/^\/+/, '') + '.html');
        const indexFile = path.join(OUT_DIR, slug.replace(/^\/+/, ''), 'index.html');
        if (fs.existsSync(htmlFile)) {
          routes.set(clean, clean + '.html');
          added++;
        } else if (fs.existsSync(indexFile)) {
          routes.set(clean, clean + '/index.html');
          added++;
        }
        // If file doesn't exist, skip — don't add a broken 200 rule
      }
      if (added) console.log(`[redirects] +${added} routes from Strapi`);
    } catch (e) {
      console.warn('[redirects] Could not fetch Strapi articles:', e.message);
    }
  } else {
    console.log('[redirects] STRAPI_URL/DOMAIN not set — using filesystem scan only.');
  }

  const lines = [
    '# Auto-generated by scripts/generate-redirects.js',
    '',
    '# Known routes — listed before the catch-all so they are never redirected.',
    '# Source: out/ directory scan (ground truth) + Strapi articles.',
  ];

  for (const [slug, target] of [...routes.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
    lines.push(`${slug}  ${target}  200`);
  }

  lines.push('');
  lines.push('# Catch-all: truly unknown paths → home with 301.');
  lines.push('# /:splat never matches bare / so root never self-redirects.');
  lines.push('/:splat  /  301');
  lines.push('/:splat/*  /  301');
  lines.push('');

  writeFile(lines.join('\n'));
  console.log(`[redirects] Written _redirects: ${routes.size} known routes + 2-rule catch-all`);
}

function writeFallback() {
  writeFile([
    '# Catch-all: unknown paths → home 301 (/:splat avoids matching root /)',
    '/:splat  /  301',
    '/:splat/*  /  301',
    '',
  ].join('\n'));
}

function writeFile(content) {
  if (!fs.existsSync(OUT_DIR)) {
    console.warn('[redirects] out/ dir not found — skipping.');
    return;
  }
  fs.writeFileSync(path.join(OUT_DIR, '_redirects'), content, 'utf8');
  console.log(content);
}

main().catch(e => {
  console.error('[redirects] Error:', e.message);
  writeFallback();
});
