/**
 * Generates out/_redirects after Next.js static export.
 *
 * Rules (processed top-to-bottom by Cloudflare Pages):
 *  - Every known page and blog route → 200 rewrite (serves the static file)
 *  - /* → / 301  (catch-all for truly unknown URLs)
 *
 * The 200 rewrites ensure Cloudflare serves the real static file and never
 * falls through to the catch-all for existing pages.
 */

const https = require('https');
const http  = require('http');
const fs    = require('fs');
const path  = require('path');

const STRAPI_URL  = process.env.NEXT_PUBLIC_STRAPI_API_URL  || '';
const STRAPI_TOKEN = process.env.NEXT_PUBLIC_STRAPI_API_TOKEN || '';
const DOMAIN      = process.env.NEXT_PUBLIC_HARDCODED_DOMAIN || '';
const OUT_DIR     = path.join(__dirname, '..', 'out');

function fetchJson(url, token) {
  return new Promise((resolve, reject) => {
    const mod = url.startsWith('https') ? https : http;
    const options = { headers: { Authorization: `Bearer ${token}` } };
    mod.get(url, options, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        try { resolve(JSON.parse(data)); }
        catch (e) { reject(e); }
      });
    }).on('error', reject);
  });
}

async function main() {
  if (!STRAPI_URL || !DOMAIN) {
    console.log('[redirects] Env vars missing — skipping _redirects generation.');
    return;
  }

  let articles = [];
  try {
    const result = await fetchJson(STRAPI_URL, STRAPI_TOKEN);
    articles = result.data || [];
  } catch (e) {
    console.warn('[redirects] Could not fetch Strapi articles:', e.message);
  }

  // Collect all known slugs for this domain
  const knownSlugs = new Set();
  for (const article of articles) {
    const slug = article?.attributes?.urlSlug;
    if (slug && article?.attributes?.Domain === DOMAIN) {
      const clean = '/' + slug.replace(/^\/+/, '');
      knownSlugs.add(clean === '/' ? '/' : clean);
    }
  }

  // Always include the root
  knownSlugs.add('/');

  const lines = [];
  lines.push('# Auto-generated by scripts/generate-redirects.js');
  lines.push('# Known routes served as static files (200 = rewrite, no URL change)');
  lines.push('');

  for (const slug of [...knownSlugs].sort()) {
    if (slug === '/') {
      lines.push('/  /index.html  200');
    } else {
      // e.g. /about → /about/index.html
      const htmlPath = slug.replace(/\/$/, '') + '/index.html';
      lines.push(`${slug}  ${htmlPath}  200`);
    }
  }

  lines.push('');
  lines.push('# Catch-all: redirect any unknown URL to home with 301');
  lines.push('/*  /  301');
  lines.push('');

  const content = lines.join('\n');

  if (!fs.existsSync(OUT_DIR)) {
    console.warn('[redirects] out/ directory not found — build may have failed.');
    return;
  }

  fs.writeFileSync(path.join(OUT_DIR, '_redirects'), content, 'utf8');
  console.log(`[redirects] Written out/_redirects with ${knownSlugs.size} known routes + catch-all.`);
  console.log(content);
}

main().catch(e => {
  console.error('[redirects] Error:', e.message);
  process.exit(0); // don't fail the build
});
